    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if(jTextArea2.getText().compareTo("") != 0)
        {
        jTabbedPane1.setSelectedIndex(1);
        JFileChooser jfc1 = new JFileChooser();
        jfc1.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int val = jfc1.showSaveDialog(this.getFrame());
        String fpath;
        if(val == JFileChooser.APPROVE_OPTION)
        {
            try
            {
                File file1 = jfc1.getSelectedFile();
                fpath = file1.getAbsolutePath()+".txt";
                File f = new File(fpath);
                FileWriter fw = new FileWriter(f);
                fw.write(jTextArea2.getText());
                fw.close();
            }
            catch(Exception e){}
        }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"No Search Results Found!", "Save", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

